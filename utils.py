

class MyUtils:
    def __init__(self):
        self.address = None
        self.name = None
        self.pw = None

    def set_name(self, name):
        self.name = name

    def get_name(self):
        return self.name

    def set_addr(self, addr):
        self.address = addr

    def get_addr(self):
        return self.address

    def set_pw(self, pw):
        self.pw = pw

    def get_pw(self):
        return self.pw
